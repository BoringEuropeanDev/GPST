def calculate_technical_indicators(prices: List[Dict]) -> Dict:
    if len(prices) < 20:
        return {}

    closes = [p["close"] for p in prices if p.get("close")]
    volumes = [p.get("volume", 0) or 0 for p in prices]

    if len(closes) < 20:
        return {}

    closes_arr = np.array(closes[-60:] if len(closes) > 60 else closes)

    sma5 = float(np.mean(closes_arr[-5:])) if len(closes_arr) >= 5 else None
    sma20 = float(np.mean(closes_arr[-20:])) if len(closes_arr) >= 20 else None
    sma50 = float(np.mean(closes_arr[-50:])) if len(closes_arr) >= 50 else None

    if len(closes_arr) >= 14:
        deltas = np.diff(closes_arr[-15:])
        gains = np.where(deltas > 0, deltas, 0)
        losses = np.where(deltas < 0, -deltas, 0)
        avg_gain = np.mean(gains) if len(gains) > 0 else 0
        avg_loss = np.mean(losses) if len(losses) > 0 else 0
        rs = avg_gain / avg_loss if avg_loss != 0 else 100
        rsi = 100 - (100 / (1 + rs))
    else:
        rsi = 50

    if len(closes_arr) >= 26:
        ema12 = float(np.mean(closes_arr[-12:]))
        ema26 = float(np.mean(closes_arr[-26:]))
        macd = ema12 - ema26
        signal = macd * 0.9
        macd_hist = macd - signal
    else:
        macd, signal, macd_hist = 0, 0, 0

    if len(closes_arr) >= 20:
        bb_mid = float(np.mean(closes_arr[-20:]))
        bb_std = float(np.std(closes_arr[-20:]))
        bb_upper = bb_mid + 2 * bb_std
        bb_lower = bb_mid - 2 * bb_std
        bb_position = (closes_arr[-1] - bb_lower) / (bb_upper - bb_lower) if (bb_upper - bb_lower) != 0 else 0.5
    else:
        bb_position = 0.5

    returns_1d = float((closes_arr[-1] - closes_arr[-2]) / closes_arr[-2]) if len(closes_arr) >= 2 else 0
    returns_5d = float((closes_arr[-1] - closes_arr[-5]) / closes_arr[-5]) if len(closes_arr) >= 5 else 0
    returns_20d = float((closes_arr[-1] - closes_arr[-20]) / closes_arr[-20]) if len(closes_arr) >= 20 else 0

    volatility = float(np.std(np.diff(closes_arr) / closes_arr[:-1])) if len(closes_arr) > 1 else 0

    avg_vol_5 = np.mean(volumes[-5:]) if len(volumes) >= 5 else 1
    avg_vol_20 = np.mean(volumes[-20:]) if len(volumes) >= 20 else 1
    volume_ratio = float(avg_vol_5 / avg_vol_20) if avg_vol_20 > 0 else 1

    return {
        "sma5": sma5, "sma20": sma20, "sma50": sma50,
        "rsi": float(rsi),
        "macd": float(macd), "macd_signal": float(signal), "macd_hist": float(macd_hist),
        "bb_position": float(bb_position),
        "returns_1d": returns_1d, "returns_5d": returns_5d, "returns_20d": returns_20d,
        "volatility": volatility, "volume_ratio": volume_ratio,
        "current_price": float(closes_arr[-1]),
    }


def score_from_technical(indicators: Dict) -> Tuple[float, List[str]]:
    score = 0.0
    reasons = []

    rsi = indicators.get("rsi", 50)
    if rsi < 30:
        score += 0.3
        reasons.append(f"RSI at {rsi:.1f} indicates oversold conditions (bullish reversal signal)")
    elif rsi > 70:
        score -= 0.3
        reasons.append(f"RSI at {rsi:.1f} indicates overbought conditions (bearish reversal signal)")
    elif rsi > 55:
        score += 0.1
        reasons.append(f"RSI at {rsi:.1f} shows moderate bullish momentum")
    elif rsi < 45:
        score -= 0.1
        reasons.append(f"RSI at {rsi:.1f} shows moderate bearish pressure")

    macd_hist = indicators.get("macd_hist", 0)
    if macd_hist > 0:
        score += 0.15
        reasons.append("MACD histogram positive (bullish momentum)")
    elif macd_hist < 0:
        score -= 0.15
        reasons.append("MACD histogram negative (bearish momentum)")

    sma5 = indicators.get("sma5")
    sma20 = indicators.get("sma20")
    current = indicators.get("current_price")
    if sma5 and sma20 and current:
        if sma5 > sma20:
            score += 0.2
            reasons.append("5-day SMA above 20-day SMA (golden cross pattern, bullish)")
        else:
            score -= 0.2
            reasons.append("5-day SMA below 20-day SMA (death cross pattern, bearish)")
        if current > sma20:
            score += 0.1
            reasons.append("Price trading above 20-day moving average (bullish)")
        else:
            score -= 0.1
            reasons.append("Price trading below 20-day moving average (bearish)")

    bb_pos = indicators.get("bb_position", 0.5)
    if bb_pos > 0.8:
        score -= 0.1
        reasons.append("Price near upper Bollinger Band (potential resistance/overbought)")
    elif bb_pos < 0.2:
        score += 0.1
        reasons.append("Price near lower Bollinger Band (potential support/oversold)")

    ret_5d = indicators.get("returns_5d", 0)
    if ret_5d > 0.05:
        score += 0.1
        reasons.append(f"Strong 5-day momentum: +{ret_5d*100:.1f}%")
    elif ret_5d < -0.05:
        score -= 0.1
        reasons.append(f"Weak 5-day momentum: {ret_5d*100:.1f}%")

    vol_ratio = indicators.get("volume_ratio", 1)
    if vol_ratio > 1.5:
        score_contribution = 0.1 if score > 0 else -0.1
        score += score_contribution
        reasons.append(f"Volume spike (ratio: {vol_ratio:.1f}x) confirms price direction")

    return max(-1.0, min(1.0, score)), reasons


def score_from_sentiment(news_items: List[Dict]) -> Tuple[float, List[str]]:
    if not news_items:
        return 0.0, []

    sentiments = [n.get("sentiment", 0) for n in news_items if "sentiment" in n]
    if not sentiments:
        return 0.0, []

    avg_sentiment = np.mean(sentiments)
    reasons = []
    recent_positive = sum(1 for s in sentiments if s > 0.2)
    recent_negative = sum(1 for s in sentiments if s < -0.2)

    if recent_positive > recent_negative:
        reasons.append(f"{recent_positive} positive news articles detected (bullish sentiment signal)")
    elif recent_negative > recent_positive:
        reasons.append(f"{recent_negative} negative news articles detected (bearish sentiment signal)")

    for item in news_items[:3]:
        if item.get("title"):
            reasons.append(f"News: \"{item['title'][:80]}...\" (Source: {item.get('source', 'Unknown')})")

    return float(max(-1, min(1, avg_sentiment * 2))), reasons


def score_from_macro(economic_data: Dict) -> Tuple[float, List[str]]:
    score = 0.0
    reasons = []

    vix = economic_data.get("VIX")
    if vix:
        if vix < 15:
            score += 0.15
            reasons.append(f"VIX at {vix:.1f} (low fear, bullish market environment)")
        elif vix > 30:
            score -= 0.25
            reasons.append(f"VIX at {vix:.1f} (high fear/volatility, bearish signal)")
        elif vix > 20:
            score -= 0.1
            reasons.append(f"VIX at {vix:.1f} (elevated fear, cautious environment)")

    yield_spread = economic_data.get("T10Y2Y")
    if yield_spread is not None:
        if yield_spread < 0:
            score -= 0.2
            reasons.append(f"Inverted yield curve (spread: {yield_spread:.2f}%) - historical recession indicator")
        elif yield_spread > 0.5:
            score += 0.1
            reasons.append(f"Positive yield curve spread ({yield_spread:.2f}%) - healthy economic expansion signal")

    fed_rate = economic_data.get("FEDFUNDS")
    if fed_rate:
        if fed_rate > 5:
            score -= 0.15
            reasons.append(f"High interest rates ({fed_rate:.2f}%) compress equity valuations")
        elif fed_rate < 2:
            score += 0.15
            reasons.append(f"Low interest rates ({fed_rate:.2f}%) support equity valuations")

    return max(-1.0, min(1.0, score)), reasons


def generate_prediction(
    ticker: str,
    technical_indicators: Dict,
    news_items: List[Dict],
    economic_data: Dict,
    sector_performance: Dict,
    profile: Optional[Dict] = None,
) -> Dict:
    sources_used = []
    all_reasons = []

    tech_score, tech_reasons = score_from_technical(technical_indicators)
    all_reasons.extend(tech_reasons)
    if technical_indicators:
        sources_used.append("Yahoo Finance historical price data (technical analysis)")

    sentiment_score, sentiment_reasons = score_from_sentiment(news_items)
    all_reasons.extend(sentiment_reasons)
    if news_items:
        sources_used.append("GDELT Global News Database")
        sources_used.append("Alpha Vantage News Sentiment API")

    macro_score, macro_reasons = score_from_macro(economic_data)
    all_reasons.extend(macro_reasons)
    if economic_data:
        sources_used.append("FRED (Federal Reserve Economic Data)")
        sources_used.append("World Bank Open Data")

    sector_score = 0.0
    sector_name = profile.get("sector", "") if profile else ""
    if sector_name and sector_name in sector_performance:
        sp = sector_performance[sector_name]
        sector_change = sp.get("change_pct", 0)
        sector_score = min(1, max(-1, sector_change / 2))
        if sector_change > 0.5:
            all_reasons.append(f"Sector ({sector_name}) up {sector_change:.2f}% today — positive sector tailwind")
            sources_used.append(f"Sector ETF performance data ({sp.get('etf', 'N/A')})")
        elif sector_change < -0.5:
            all_reasons.append(f"Sector ({sector_name}) down {sector_change:.2f}% today — sector headwind")

    if technical_indicators:
        composite = (tech_score * 0.40) + (sentiment_score * 0.30) + (macro_score * 0.20) + (sector_score * 0.10)
    else:
        composite = (sentiment_score * 0.50) + (macro_score * 0.35) + (sector_score * 0.15)

    volatility = technical_indicators.get("volatility", 0.02)
    confidence_penalty = min(0.3, volatility * 5)

    raw_prob_up = (composite + 1) / 2
    prob_up = min(0.85, max(0.15, raw_prob_up))
    prob_down = min(0.85, max(0.15, 1 - raw_prob_up))

    total = prob_up + prob_down
    prob_up /= total
    prob_down /= total
    prob_neutral = max(0, 1 - prob_up - prob_down) * 0.15

    total = prob_up + prob_down + prob_neutral
    prob_up /= total
    prob_down /= total
    prob_neutral /= total

    if prob_up > 0.5 and prob_up > prob_down + 0.05:
        direction = "UP"
        confidence = prob_up
    elif prob_down > 0.5 and prob_down > prob_up + 0.05:
        direction = "DOWN"
        confidence = prob_down
    else:
        direction = "NEUTRAL"
        confidence = prob_neutral + 0.5

    confidence = max(0.5, min(0.95, confidence))
    estimated_change = composite * volatility * 100 * 2

    rationale = f"""
**Predictive Analysis for {ticker}**

Direction: {direction} | Confidence: {confidence*100:.1f}% | Estimated Move: {estimated_change:+.2f}%

**Technical Signals ({int(tech_score*50+50)}/100):**
{chr(10).join('• ' + r for r in tech_reasons) if tech_reasons else '• Insufficient historical data for full technical analysis'}

**News & Sentiment ({int(sentiment_score*50+50)}/100):**
{chr(10).join('• ' + r for r in sentiment_reasons) if sentiment_reasons else '• No significant news detected'}

**Macroeconomic Context ({int(macro_score*50+50)}/100):**
{chr(10).join('• ' + r for r in macro_reasons) if macro_reasons else '• Macro data unavailable'}

**Data Sources Used:**
{chr(10).join('• ' + s for s in set(sources_used)) if sources_used else '• Yahoo Finance'}

⚠️ {DISCLAIMER}
""".strip()

    return {
        "ticker": ticker,
        "prediction_date": datetime.utcnow().isoformat(),
        "target_date": (datetime.utcnow() + timedelta(days=1)).isoformat(),
        "predicted_direction": direction,
        "predicted_change_pct": round(estimated_change, 3),
        "confidence": round(confidence, 3),
        "probability_up": round(prob_up, 3),
        "probability_down": round(prob_down, 3),
        "probability_neutral": round(prob_neutral, 3),
        "rationale": rationale,
        "sources_used": list(set(sources_used)),
        "features_used": {
            "technical_score": round(tech_score, 3),
            "sentiment_score": round(sentiment_score, 3),
            "macro_score": round(macro_score, 3),
            "sector_score": round(sector_score, 3),
            "composite_score": round(composite, 3),
            "volatility": round(volatility, 4),
        },
        "model_version": MODEL_VERSION,
        "disclaimer": DISCLAIMER,
    }


class PredictionEngine:
    def __init__(self):
        self._economic_cache = {}
        self._sector_cache = {}
        self._cache_time = None

    async def predict_ticker(self, ticker: str) -> Optional[Dict]:
        from app.services.data_ingestion import DataIngestionService
        ingestion = DataIngestionService()

        try:
            history, profile, av_news, gdelt_news = await asyncio.gather(
                ingestion.get_yahoo_history(ticker, "1y"),
                ingestion.get_yahoo_profile(ticker),
                ingestion.get_alpha_vantage_news(ticker),
                ingestion.get_gdelt_news(ticker),
                return_exceptions=True
            )

            history = history if isinstance(history, list) else []
            profile = profile if isinstance(profile, dict) else {}
            av_news = av_news if isinstance(av_news, list) else []
            gdelt_news = gdelt_news if isinstance(gdelt_news, list) else []
            all_news = av_news + gdelt_news

            if not self._economic_cache or (
                self._cache_time and
                (datetime.utcnow() - self._cache_time).seconds > 3600
            ):
                await self._refresh_macro_data(ingestion)

            indicators = calculate_technical_indicators(history)
            prediction = generate_prediction(
                ticker=ticker,
                technical_indicators=indicators,
                news_items=all_news,
                economic_data=self._economic_cache,
                sector_performance=self._sector_cache,
                profile=profile,
            )
            await ingestion.close()
            return prediction
        except Exception as e:
            logger.error(f"Prediction error for {ticker}: {e}")
            await ingestion.close()
            return None

    async def _refresh_macro_data(self, ingestion):
        try:
            vix_data = await ingestion.get_fred_data("VIXCLS", 1)
            yield_data = await ingestion.get_fred_data("T10Y2Y", 1)
            fed_data = await ingestion.get_fred_data("FEDFUNDS", 1)

            if vix_data:
                self._economic_cache["VIX"] = vix_data[0].get("value")
            if yield_data:
                self._economic_cache["T10Y2Y"] = yield_data[0].get("value")
            if fed_data:
                self._economic_cache["FEDFUNDS"] = fed_data[0].get("value")

            self._sector_cache = await ingestion.get_sector_etf_data()
            self._cache_time = datetime.utcnow()
        except Exception as e:
            logger.error(f"Error refreshing macro data: {e}")

    async def run_predictions(self):
        logger.info("Running scheduled predictions...")
        from app.database import AsyncSessionLocal, Stock, Prediction
        from sqlalchemy import select

        async with AsyncSessionLocal() as session:
            result = await session.execute(select(Stock).where(Stock.is_active == True).limit(50))
            stocks = result.scalars().all()

        for stock in stocks[:20]:
            try:
                pred = await self.predict_ticker(stock.ticker)
                if pred:
                    async with AsyncSessionLocal() as session:
                        prediction = Prediction(
                            ticker=pred["ticker"],
                            prediction_date=datetime.utcnow(),
                            target_date=datetime.utcnow() + timedelta(days=1),
                            predicted_direction=pred["predicted_direction"],
                            predicted_change_pct=pred["predicted_change_pct"],
                            confidence=pred["confidence"],
                            probability_up=pred["probability_up"],
                            probability_down=pred["probability_down"],
                            probability_neutral=pred["probability_neutral"],
                            rationale=pred["rationale"],
                            sources_used=pred["sources_used"],
                            features_used=pred["features_used"],
                            model_version=pred["model_version"],
                        )
                        session.add(prediction)
                        await session.commit()
                await asyncio.sleep(2)
            except Exception as e:
                logger.error(f"Error predicting {stock.ticker}: {e}")

    async def evaluate_past_predictions(self):
        logger.info("Evaluating past predictions...")
        from app.database import AsyncSessionLocal, Prediction, ModelMetrics
        from sqlalchemy import select, update
        from app.services.data_ingestion import DataIngestionService

        ingestion = DataIngestionService()

        try:
            async with AsyncSessionLocal() as session:
                yesterday = datetime.utcnow() - timedelta(days=1)
                result = await session.execute(
                    select(Prediction).where(
                        Prediction.target_date <= datetime.utcnow(),
                        Prediction.was_correct == None,
                        Prediction.prediction_date >= yesterday - timedelta(days=7)
                    ).limit(100)
                )
                predictions = result.scalars().all()

                correct = 0
                evaluated = 0
                for pred in predictions:
                    try:
                        quote = await ingestion.get_yahoo_quote(pred.ticker)
                        if quote and quote.get("current_price") and quote.get("previous_close"):
                            actual_change = (quote["current_price"] - quote["previous_close"]) / quote["previous_close"]
                            actual_dir = "UP" if actual_change > 0.005 else "DOWN" if actual_change < -0.005 else "NEUTRAL"
                            was_correct = actual_dir == pred.predicted_direction
                            if was_correct:
                                correct += 1
                            evaluated += 1
                            await session.execute(
                                update(Prediction).where(Prediction.id == pred.id).values(
                                    actual_direction=actual_dir,
                                    actual_change_pct=actual_change * 100,
                                    was_correct=was_correct,
                                )
                            )
                    except Exception as e:
                        logger.error(f"Evaluation error for {pred.ticker}: {e}")

                if evaluated > 0:
                    accuracy = correct / evaluated
                    session.add(ModelMetrics(
                        model_version=MODEL_VERSION,
                        evaluation_date=datetime.utcnow(),
                        accuracy=accuracy,
                        total_predictions=evaluated,
                        correct_predictions=correct,
                        precision=accuracy, recall=accuracy, f1_score=accuracy,
                    ))
                    logger.info(f"Prediction accuracy: {accuracy:.2%} ({correct}/{evaluated})")

                await session.commit()
        finally:
            await ingestion.close()
