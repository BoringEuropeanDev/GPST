"""
Predictions API - Stock direction predictions
ALL predictions include disclaimer: NOT FINANCIAL ADVICE

Fixes vs original:
  - /model/metrics registered BEFORE /{ticker} to avoid route conflict
  - Uses shared prediction_engine singleton (same macro/sector cache as scheduler)
  - Cache TTL reduced to 20 min; supports ?refresh=true to force fresh prediction
"""
from fastapi import APIRouter, Depends, HTTPException, Query
from sqlalchemy.ext.asyncio import AsyncSession
from sqlalchemy import select, desc
from datetime import datetime, timedelta
from typing import Optional

from app.database import get_db, Prediction, ModelMetrics, MLModelMetric
from app.services.prediction_engine import prediction_engine       # shared singleton

router = APIRouter()

DISCLAIMER = (
    "This is not financial advice. All predictions are generated by an automated "
    "system for informational and educational purposes only. Past performance does "
    "not guarantee future results. Always consult a licensed financial advisor "
    "before making any investment decisions."
)

# ── /model/metrics MUST come before /{ticker} ─────────────────────────────────

@router.get("/model/metrics")
async def get_model_metrics(
    ticker:     Optional[str] = Query(None),
    model_type: Optional[str] = Query(None),
    db: AsyncSession = Depends(get_db),
):
    """
    Returns ML training metrics (val_accuracy, val_auc, hit rates) plus
    post-hoc prediction accuracy from ModelMetrics.
    """
    ml_query = select(MLModelMetric).order_by(desc(MLModelMetric.created_at))
    if ticker:
        ml_query = ml_query.where(MLModelMetric.ticker == ticker.upper())
    if model_type:
        ml_query = ml_query.where(MLModelMetric.model_type == model_type)

    ml_result   = await db.execute(ml_query.limit(20))
    ml_metrics  = ml_result.scalars().all()

    acc_result  = await db.execute(
        select(ModelMetrics).order_by(desc(ModelMetrics.evaluation_date)).limit(10)
    )
    acc_metrics = acc_result.scalars().all()

    return {
        "ml_metrics": [
            {
                "ticker":         m.ticker,
                "model_type":     m.model_type,
                "train_end_date": m.train_end_date.isoformat() if m.train_end_date else None,
                "val_accuracy":   m.val_accuracy,
                "val_auc":        m.val_auc,
                "hit_rate_up":    m.hit_rate_up,
                "hit_rate_down":  m.hit_rate_down,
                "samples":        m.samples,
                "created_at":     m.created_at.isoformat() if m.created_at else None,
            }
            for m in ml_metrics
        ],
        "accuracy_metrics": [
            {
                "model_version":       m.model_version,
                "evaluation_date":     m.evaluation_date.isoformat() if m.evaluation_date else None,
                "accuracy":            m.accuracy,
                "total_predictions":   m.total_predictions,
                "correct_predictions": m.correct_predictions,
                "f1_score":            m.f1_score,
            }
            for m in acc_metrics
        ],
        "disclaimer": DISCLAIMER,
    }


# ── /{ticker} ─────────────────────────────────────────────────────────────────

@router.get("/{ticker}")
async def get_prediction(
    ticker:  str,
    refresh: bool = Query(False, description="Force a fresh prediction, bypassing cache"),
    db: AsyncSession = Depends(get_db),
):
    ticker = ticker.upper()

    # Cache window: 20 minutes (reduced from 1 hour)
    if not refresh:
        cache_cutoff = datetime.utcnow() - timedelta(minutes=20)
        result = await db.execute(
            select(Prediction)
            .where(
                Prediction.ticker          == ticker,
                Prediction.prediction_date >= cache_cutoff,
            )
            .order_by(desc(Prediction.prediction_date))
            .limit(1)
        )
        cached_pred = result.scalar_one_or_none()

        if cached_pred:
            return {
                "ticker":               cached_pred.ticker,
                "prediction_date":      cached_pred.prediction_date.isoformat(),
                "target_date":          cached_pred.target_date.isoformat(),
                "predicted_direction":  cached_pred.predicted_direction,
                "predicted_change_pct": cached_pred.predicted_change_pct,
                "confidence":           cached_pred.confidence,
                "probability_up":       cached_pred.probability_up,
                "probability_down":     cached_pred.probability_down,
                "probability_neutral":  cached_pred.probability_neutral,
                "rationale":            cached_pred.rationale,
                "sources_used":         cached_pred.sources_used,
                "features_used":        cached_pred.features_used,
                "model_version":        cached_pred.model_version,
                "disclaimer":           DISCLAIMER,
                "cached":               True,
            }

    prediction = await prediction_engine.predict_ticker(ticker)

    if not prediction:
        raise HTTPException(status_code=503, detail="Prediction service unavailable. Please try again.")

    try:
        db.add(Prediction(
            ticker               = ticker,
            prediction_date      = datetime.utcnow(),
            target_date          = datetime.utcnow() + timedelta(days=1),
            predicted_direction  = prediction["predicted_direction"],
            predicted_change_pct = prediction["predicted_change_pct"],
            confidence           = prediction["confidence"],
            probability_up       = prediction["probability_up"],
            probability_down     = prediction["probability_down"],
            probability_neutral  = prediction["probability_neutral"],
            rationale            = prediction["rationale"],
            sources_used         = prediction["sources_used"],
            features_used        = prediction["features_used"],
            model_version        = prediction["model_version"],
        ))
        await db.commit()
    except Exception:
        pass

    prediction["disclaimer"] = DISCLAIMER
    prediction["cached"]     = False
    return prediction


@router.get("/{ticker}/history")
async def get_prediction_history(ticker: str, db: AsyncSession = Depends(get_db)):
    ticker = ticker.upper()
    result = await db.execute(
        select(Prediction)
        .where(Prediction.ticker == ticker)
        .order_by(desc(Prediction.prediction_date))
        .limit(30)
    )
    predictions = result.scalars().all()

    return {
        "ticker": ticker,
        "predictions": [
            {
                "date":                  p.prediction_date.isoformat(),
                "direction":             p.predicted_direction,
                "confidence":            p.confidence,
                "actual_direction":      p.actual_direction,
                "was_correct":           p.was_correct,
                "predicted_change_pct":  p.predicted_change_pct,
                "actual_change_pct":     p.actual_change_pct,
            }
            for p in predictions
        ],
        "disclaimer": DISCLAIMER,
    }
